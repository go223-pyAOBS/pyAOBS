C----------------------------------------------------------------------
      SUBROUTINE DRIVER(MODES,PARMS)
      INTEGER MODES(5)
      REAL*4 PARMS(6)
      LOGICAL NOPRNT
      INTEGER LIST(10)
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      REAL*4 TEMP,FLR
      INTEGER MODEL,NUPAGE,NCHANG
      MODEL=MODES(1)
      NOPRNT=.FALSE.
      NUPAGE=1
      NCHANG=-1
C INITIALIZE PHASE FRACTIONS IF REQUESTED
      FLR=1.0
      IF(MODES(2).NE.0) GOTO 20
      DO 10 K=1,NPHAS
10    FA(K)=0.0
20    DO 30 J=1,NCOMP
30    CLJ(J)=CSJ(J)
C SET START TEMPERATURE
      TEMP=PARMS(1)+PARMS(5)
C MAIN TEMPERATURE LOOP
100   CONTINUE
      CALL STATE(NL,LIST,NERR)
      GOTO(1100,1200,1300),MODEL
C MODEL 1: EQUILIBRIUM CRYSTAL/MELT
1100  FLR=1.0
      GOTO 110
C MODEL 2: FRACTIONAL CRYSTALLIZATION
1200  FLR=FLR*FL
      GOTO 110
C MODEL3: FRACTIONAL MELTING
1300  FLR=FLR/(1.0-FL)
110   IF(NL.NE.0) NOPRNT=.FALSE.
C CHECK IF OUTPUT REQUIRED
      IF(MODES(5).NE.0) GOTO 150
      NT=NCHANG
      NCHANG=0
      DO 140 K=1,NL
140   NCHANG=NCHANG+LIST(K)
      IF(NCHANG.EQ.NT) GOTO 200
150   IF(PARMS(4).LE.0.) GOTO 160
      TABS=TEMP-PARMS(1)
      TAMOD=MOD(TABS,PARMS(4))
      IF(ABS(TAMOD).GT.0.001) GOTO 200
160   CALL PRNTER(MODES,TEMP,FLR,NERR,NUPAGE)
200   IF(NERR.NE.0) RETURN
      IF(MODES(3).EQ.0) GOTO 200
C ABORT IF USER KEY PRESSED
      IF(INKEY(0).NE.0) RETURN
C UPDATE TEMPERATURE
      TEMP=TEMP+PARMS(3)
      IF((PARMS(2)-TEMP)/PARMS(3).LE.0.) RETURN
C RESET COMPOSITION FOR NEXT STEP
      GOTO(2100,2200,2300),MODEL
2100  FLR=1.0
      GOTO 100
2200  DO 2210 J=1,NCOMP
2210  CSJ(J)=CLJ(J)
      GOTO 100
2300  DO 2310 J=1,NCOMP
2310  CSJ(J)=(CSJ(J)-CLJ(J)*FL)/(1.0-FL)
      GOTO 10
      END
