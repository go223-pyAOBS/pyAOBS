      SUBROUTINE PRNTER(MODES,TEMP,FLR,NERR,NUPAGE)
      INTEGER MODES(5),NUPAGE,NERR
      REAL*4 TEMP,FLR
      COMMON/SYSIO/ NCON, NLST, NDSK
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      INTEGER NWRT
      CALL CIMPL
      NWRT=NCON
C FULL OUTPUT MODE
      IF(MODES(4).NE.0) GOTO 300
      IF(NUPAGE.NE.0) THEN
        WRITE(NWRT,9200) (PNAMEA(J),J=1,NPHAS)
9200    FORMAT(1H0,'SYS',4X,'LIQ ',3(3X,A4),5X,5(3X,A4))
      ENDIF
      WRITE(NWRT,9210) FL,(FA(J),J=1,NPHAS)
9210  FORMAT(' FRAC ',7X,4F7.4,5X,5F7.4)
      DO 220 K=1,NCOMPT
220   WRITE(NWRT,9220) CNAME(K),CSJ(K),CLJ(K),(CAJ(J,K),J=1,NPHAS)
9220  FORMAT(1H ,A4,1X,5F7.4,5X,5F7.4)
      WRITE(NWRT,9230) TEMP,FLR
9230  FORMAT(1H ,'TEMP=',F10.4,5X,'FLR =',F10.4)
      WRITE(NWRT,9240) (SNAMES(K),SPARM(K),K=1,NS)
9240  FORMAT(1H ,4(A4,'=',F10.4,5X))
      GOTO 400
C SUMMARY ONLY OUTPUT
300   IF(NUPAGE.NE.0) THEN
        WRITE(NWRT,9300) (PNAMEA(K),K=1,NPHAS),(CNAMEJ(J),J=1,NCOMPT)
9300    FORMAT(1H0,'TEMP',4X,'FLR',4X,3(3X,A4),5X,5(3X,A4))
      ENDIF
      WRITE(NWRT,9310) TEMP,FLR,(FA(J),J=1,NPHAS),(CLJ(J),J=1,NCOMPT)
9310  FORMAT(1H ,F5.0,5F7.4,3(5X,5F7.4))
400   IF(NERR.EQ.1) WRITE(NWRT,9400)
9400  FORMAT('0MATRIX INVERSION PROBLEM IN STATE')
      IF(NERR.EQ.2) WRITE(NWRT,9410)
9410  FORMAT('0MAXIMUM ITERATIONS REACHED IN STATE')
C PRINT OUTPUT TO PRINTER IF ENABLED
      IF(MODES(3).EQ.0) GOTO 500
      NWRT=NLST
      NUPAGE=0
      GOTO 100
500   NUPAGE=0
      RETURN
      END
C----------------------------------------------------------------------
      SUBROUTINE STATE(NL,LIST,NERR)
      INTEGER NL,LIST(10),NERR
      REAL*4 DFA(10)
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      INTEGER ITER,MAX
      REAL*4 TOL,TST
      DATA MAX/20/,TOL/1.E-5/
      NERR=0
      CALL KDCALC(1)
C NEWTON-RAPHSON MAIN LOOP
      DO 500 ITER=1,MAX
C CALC RJ, LIQUID COMPOSITION CLJ
      DO 100 J=1,NCOMP
      T=1.0
      DO 50 L=1,NPHAS
50    T=T+FA(L)*(FKDAJ(L,J)-1.0)
100   RJ(J)=1.0/T
      CL(J)=CSJ(J)*RJ(J)
      DO 60 L=1,NPHAS
60    CA(L,J)=FKDAJ(L,J)*CL(J)
C EVALUATE SATURATION QA FOR ALL PHASES
      NL=0
      DO 200 L=1,NPHAS
      TQ=-TA(L)
      DO 150 J=1,NCOMP
150   TQ=TQ+UA(L,J)*CA(L,J)
      QA(L)=TQ
      IF(TQ.GT.0.0) GOTO 200
      CALL STOICH(L,ISOK)
      IF(ISOK.EQ.0) GOTO 200
      NL=NL+1
      LIST(NL)=L
200   CONTINUE
C NO STABLE MINERALS, EXIT
      IF(NL.EQ.0) GOTO 700
C BUILD DERIVATIVE MATRIX PAB
      DO 300 J=1,NL
      L=LIST(J)
      DFA(J)=QA(L)
      DO 280 K=1,NL
      M=LIST(K)
      T=0.0
      DO 260 I=1,NCOMP
      TU=UA(L,I)
      IF(TU.EQ.0.0) GOTO 260
      T=T+TU*CA(M,I)*RJ(I)*RJ(I)*(FKDAJ(M,I)-1.0)
260   CONTINUE
      PAB(J,K)=-T
280   CONTINUE
300   CONTINUE
C SOLVE LINEAR SYSTEM PAB*DF = -QA
      CALL MATEQ(PAB,NL,DFA,DET,NPDIM)
      IF(ABS(DET).LT.1.E-10) GOTO 900
C UPDATE PHASE FRACTIONS FA
      TST=0.0
      FL=1.0
      DO 350 J=1,NL
      L=LIST(J)
      T=FA(L)+DFA(J)
      IF(T.LT.0.0) T=0.0
      IF(T.GT.1.0) T=0.9999
      TST=TST+ABS(FA(L)-T)
      FA(L)=T
      FL=FL-T
350   CONTINUE
C CONVERGENCE CHECK
      IF(TST.LE.TOL) GOTO 700
C UPDATE COMPOSITION DEPENDENT KD
      CALL KDCALC(3)
500   CONTINUE
C MAX ITERATIONS REACHED, NO CONVERGENCE
      NERR=2
      RETURN
700   NERR=0
      RETURN
C SINGULAR MATRIX ERROR
900   NERR=1
      RETURN
      END
C----------------------------------------------------------------------
      SUBROUTINE KDCALC(KDMODE)
      INTEGER KDMODE
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      REAL*4 TK,AN,ARHENF
      ARHENF(A,B,T)=EXP(2.302585*(A/T))+B
      TK=SPARM(1)
C KDMODE=1: FULL RESET ALL KD VALUES
      IF(KDMODE.EQ.1) THEN
        DO 120 L=1,NPHAS
        DO 120 J=1,NCOMP
120     FKDAJ(L,J)=0.0
      ENDIF
C KDMODE=2: UPDATE TEMP DEPENDENT KD
      IF(KDMODE.GE.2) THEN
C OLIVINE KD (PHASE 2)
        FKDAJ(2,3)=ARHENF(2715.,-1.158,TK)
        FKDAJ(2,4)=ARHENF(4230.,-2.741,TK)
C CLINOPYROXENE KD (PHASE3)
        FKDAJ(3,3)=ARHENF(3798.,-2.28,TK)
        FKDAJ(3,4)=0.24*FKDAJ(3,3)
        FKDAJ(3,5)=ARHENF(1738.,-0.753,TK)
        FKDAJ(3,6)=ARHENF(1034.,-1.27,TK)
        FKDAJ(3,1)=ARHENF(2418.,-2.30,TK)
        FKDAJ(3,2)=ARHENF(5087.,-4.48,TK)
      ENDIF
C KDMODE=3: UPDATE COMPOSITION DEPENDENT KD (PLAGIOCLASE PHASE1)
      IF(KDMODE.EQ.3) THEN
        AN=CL(1)/(CL(1)+1.5*CL(2))
        FKDAJ(1,1)=ARHENF(2446.,-(1.122+0.2562*AN),TK)
        FKDAJ(1,2)=ARHENF((3195+3283*AN),-(2.318+1.885*AN),TK)
      ENDIF
      RETURN
      END
C----------------------------------------------------------------------
      SUBROUTINE STOICH(JP,ISTABL)
      INTEGER JP,ISTABL
      ISTABL=1
C ONLY CPX (JP=3) HAS ADDITIONAL STABILITY CHECK IN THIS CODE
      IF(JP.NE.3) GOTO 900
      T=2.0*(CAJ(3,3)+CAJ(3,4))+CAJ(3,5)
      IF(T.LT.1.0) ISTABL=0
900   RETURN
      END
C----------------------------------------------------------------------
      SUBROUTINE CIMPL
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      INTEGER I,IMP,IMC
      REAL*4 S
C ZERO IMPLICIT SILICA COMPONENTS
      IF(NCIMP.EQ.0) GOTO 130
      DO 100 J=NCOMP+1,NCOMPT
100   CLJ(J)=0.0
      DO 100 K=1,NPHAS
      CAJ(K,J)=0.0
C CALC IMPLICIT SILICA TERMS
130   DO 150 I=1,IMPDIM
      IMP=IMPL(I)
      IF(IMP.EQ.0) GOTO 200
      IMC=IMCL(I)
      S=DA0(I)
      DO 140 J=1,NCOMP
140   S=S+DAJ(I,J)*CAJ(IMP,J)
      CAJ(IMP,IMC)=S
150   CONTINUE
200   DO 300 J=1,NCOMPT
      S=CSJ(J)
      DO 250 K=1,NPHAS
250   S=S-CAJ(K,J)*FA(K)
300   CLJ(J)=S
      RETURN
      END
C----------------------------------------------------------------------
      SUBROUTINE MATEQ(A,N,Y,DET,NDIM)
      INTEGER N,NDIM,I,J,K,IMX,ICHG(40)
      REAL*4 A(NDIM,NDIM),Y(NDIM),DET,AMX,TEMP
      DET=1.0
      DO 10 K=1,N
C FIND PIVOT ROW
      AMX=ABS(A(K,K))
      IMX=K
      DO 1 I=K,N
      IF(ABS(A(I,K)).LE.AMX) GOTO 1
      AMX=ABS(A(I,K))
      IMX=I
1     CONTINUE
C SINGULAR MATRIX CHECK
      IF(AMX.LT.1.E-8) THEN
        DET=0.0
        RETURN
      ENDIF
C SWAP ROWS IF NEEDED
      IF(IMX.NE.K) THEN
        DO 4 J=1,N
        TEMP=A(K,J)
        A(K,J)=A(IMX,J)
4       A(IMX,J)=TEMP
        TEMP=Y(K)
        Y(K)=Y(IMX)
        Y(IMX)=TEMP
        ICHG(K)=IMX
        DET=-DET
      ELSE
        ICHG(K)=K
      ENDIF
C NORMALIZE PIVOT ROW
      AKK=A(K,K)
      DET=DET*AKK
      A(K,K)=1.0/AKK
      DO 7 J=1,N
7       A(K,J)=A(K,J)*A(K,K)
      Y(K)=Y(K)*A(K,K)
C ELIMINATE OTHER ROWS
      DO 9 I=1,N
      IF(I.EQ.K) GOTO 9
      AKI=A(I,K)
      DO 8 J=1,N
8       A(I,J)=A(I,J)-AKI*A(K,J)
9       Y(I)=Y(I)-AKI*Y(K)
10    CONTINUE
C REORDER SOLUTION VECTOR
      DO 13 K=1,N
      L=N+1-K
      KI=ICHG(L)
      IF(L.EQ.KI) GOTO 13
      DO 12 I=1,N
12    TEMP=A(I,L)
      A(I,L)=A(I,KI)
      A(I,KI)=TEMP
13    CONTINUE
      RETURN
      END
