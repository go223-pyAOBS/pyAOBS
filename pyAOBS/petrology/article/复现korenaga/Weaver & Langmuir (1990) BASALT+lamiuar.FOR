C=====================================================================
C DRIVER SUBROUTINE: EXTENDED WITH MULTI-PRESSURE LOOP
C MODES(6)=1: Polybaric crystallization enabled
C=====================================================================
      SUBROUTINE DRIVER(MODES,PARMS)
      INTEGER MODES(6)
      REAL*4 PARMS(6)
      LOGICAL NOPRNT
      INTEGER LIST(10)
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      REAL*4 TEMP,FLR,PHI,PHI_H,PHI_L,DP
      INTEGER MODEL,NUPAGE,NCHANG,IP
      MODEL=MODES(1)
      NOPRNT=.FALSE.
      NUPAGE=1
      NCHANG=-1
      FLR=1.0
C PRESSURE PARAMS
      PHI_H=PARMS(5)
      PHI_L=PARMS(6)
      DP=PARMS(4)
C INITIALIZE PHASES
      IF(MODES(2).NE.0) GOTO 20
      DO 10 K=1,NPHAS
10    FA(K)=0.0
20    DO 30 J=1,NCOMP
30    CLJ(J)=CSJ(J)
C SINGLE PRESSURE MODE (ORIGINAL 1atm code)
      IF(MODES(6).EQ.0) THEN
        SPARM(2)=0.0
        TEMP=PARMS(1)+PARMS(5)
        CALL TEMPRUN(TEMP,PARMS,MODEL,NUPAGE,NCHANG,NOPRNT)
        GOTO 999
      ENDIF
C NEW POLYBARIC LOOP: MULTI-PRESSURE SEGMENT CRYSTALLIZATION
      PHI=PHI_H
90    IF(PHI.LT.PHI_L-0.01) GOTO 999
      SPARM(2)=PHI
      TEMP=PARMS(1)+PARMS(5)
      CALL TEMPRUN(TEMP,PARMS,MODEL,NUPAGE,NCHANG,NOPRNT)
C UPDATE MELT FOR NEXT LOWER PRESSURE STEP
      DO 95 J=1,NCOMP
95    CSJ(J)=CLJ(J)
      PHI=PHI-DP
      GOTO 90
999   RETURN
      END
C=====================================================================
C SUBROUTINE TEMPRUN: INNER TEMPERATURE LOOP (EXTRACTED FROM DRIVER)
C=====================================================================
      SUBROUTINE TEMPRUN(TEMP,PARMS,MODEL,NUPAGE,NCHANG,NOPRNT)
      REAL*4 TEMP,PARMS(6),FLR
      INTEGER MODEL,NUPAGE,NCHANG
      LOGICAL NOPRNT
      INTEGER LIST(10)
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10)
100   CONTINUE
      SPARM(1)=TEMP
      CALL KDCALC(4) ! KDMODE=4: T+P DEPENDENT Kd
      CALL STATE(NL,LIST,NERR)
      GOTO(1100,1200,1300),MODEL
1100  FLR=1.0
      GOTO 110
1200  FLR=FLR*FL
      GOTO 110
1300  FLR=FLR/(1.0-FL)
110   IF(NL.NE.0) NOPRNT=.FALSE.
      IF(MODES(5).NE.0) GOTO 150
      NT=NCHANG
      NCHANG=0
      DO 140 K=1,NL
140   NCHANG=NCHANG+LIST(K)
      IF(NCHANG.EQ.NT) GOTO 200
150   IF(PARMS(4).LE.0.) GOTO 160
      TABS=TEMP-PARMS(1)
      TAMOD=MOD(TABS,PARMS(4))
      IF(ABS(TAMOD).GT.0.001) GOTO 200
160   CALL PRNTER(MODES,TEMP,FLR,NERR,NUPAGE)
200   IF(NERR.NE.0) RETURN
      IF(INKEY(0).NE.0) RETURN
      TEMP=TEMP+PARMS(3)
      IF((PARMS(2)-TEMP)/PARMS(3).LE.0.) RETURN
C UPDATE COMPOSITION FOR NEXT TEMP STEP
      GOTO(2100,2200,2300),MODEL
2100  FLR=1.0
      GOTO 100
2200  DO 2210 J=1,NCOMP
2210  CSJ(J)=CLJ(J)
      GOTO 100
2300  DO 2310 J=1,NCOMP
2310  CSJ(J)=(CSJ(J)-CLJ*FL)/(1.0-FL)
      GOTO 100
      END
C=====================================================================
C KDCALC EXTENDED: ADD PRESSURE CORRECTION TERM FOR ALL Kd
C KDMODE=1: FULL RESET; 2:T ONLY;3:COMP ONLY;4:T+P DEPENDENT (NEW)
C=====================================================================
      SUBROUTINE KDCALC(KDMODE)
      INTEGER KDMODE
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      REAL*4 TK,P,ARHENF,A,B,CP
      ARHENF(A,B,CP,T)=10.0**(A/T + B + CP*P)
      TK=SPARM(1)
      P=SPARM(2)
C RESET ALL Kd TO ZERO
      IF(KDMODE.EQ.1) THEN
        DO 120 L=1,NPHAS
        DO 120 J=1,NCOMP
120     FKDAJ(L,J)=0.0
      ENDIF
C T+P DEPENDENT UPDATE (KDMODE=2/4)
      IF(KDMODE.GE.2) THEN
C OLIVINE (PHASE 2) MgO: A=3740,B=-1.87,C=0.0008
        FKDAJ(2,3)=ARHENF(3740.,-1.87,0.0008,TK)
C OLIVINE FeO: A=3911,B=-2.50,C=0.0006
        FKDAJ(2,4)=ARHENF(3911.,-2.50,0.0006,TK)
C CPX MgO A=3798,B=-2.28,C=0.004
        FKDAJ(3,3)=ARHENF(3798.,-2.28,0.004,TK)
C CPX FeO=0.24*KdMg
        FKDAJ(3,4)=0.24*FKDAJ(3,3)
C CPX CaAl2O4 A=1738,B=-0.753,C=0.009
        FKDAJ(3,5)=ARHENF(1738.,-0.753,0.009,TK)
C CPX TiO2 A=1034,B=-1.27,C=0.005
        FKDAJ(3,6)=ARHENF(1034.,-1.27,0.005,TK)
C CPX NaAlO2 A=2418,B=-2.30,C=0.006
        FKDAJ(3,1)=ARHENF(2418.,-2.30,0.006,TK)
C CPX FeO2 A=5087,B=-4.48,C=0.003
        FKDAJ(3,2)=ARHENF(5087.,-4.48,0.003,TK)
      ENDIF
C PLAGIOCLASE P+COMP DEPENDENT (KDMODE=3/4)
      IF(KDMODE.EQ.3 .OR. KDMODE.EQ.4) THEN
        AN=CL(1)/(CL(1)+1.5*CL(2))
C Plag CaAl A=2446,B=-(1.122+0.2562*AN),C=0.012
        FKDAJ(1,1)=10.0**(2446./TK -(1.122+0.2562*AN)+0.012*P)
C Plag NaAl A=3195+3283*AN,B=-(2.318+1.885*AN),C=0.007
        FKDAJ(1,2)=10.0**((3195+3283*AN)/TK -(2.318+1.885*AN)+0.007*P)
      ENDIF
      RETURN
      END
C=====================================================================
C STOICH: UPDATED PRESSURE DEPENDENT CPX SATURATION JUDGE
C=====================================================================
      SUBROUTINE STOICH(JP,ISTABL)
      INTEGER JP,ISTABL
      REAL*4 T,P
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10,10)
      ISTABL=1
      IF(JP.NE.3) GOTO 900
      P=SPARM(2)
      T=2.0*(CAJ(3,3)+CAJ(3,4))+CAJ(3,5)
C HIGH P>4kbar higher saturation threshold for Cpx
      IF(P.GT.4.0) THEN
        IF(T.LT.1.1) ISTABL=0
      ELSE
        IF(T.LT.1.0) ISTABL=0
      ENDIF
900   RETURN
      END
C=====================================================================
C ORIGINAL UNCHANGED SUBROUTINES (CIMPL, MATEQ, PRNTER, STATE)
C NO MODIFICATION NEEDED
C=====================================================================
      SUBROUTINE CIMPL
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      INTEGER I,IMP,IMC
      REAL*4 S
      IF(NCIMP.EQ.0) GOTO 130
      DO 100 J=NCOMP+1,NCOMPT
100   CLJ(J)=0.0
      DO 100 K=1,NPHAS
      CAJ(K,J)=0.0
130   DO 150 I=1,IMPDIM
      IMP=IMPL(I)
      IF(IMP.EQ.0) GOTO 200
      IMC=IMCL(I)
      S=DA0(I)
      DO 140 J=1,NCOMP
140   S=S+DAJ(I,J)*CAJ(IMP,J)
      CAJ(IMP,IMC)=S
150   CONTINUE
200   DO 300 J=1,NCOMPT
      S=CSJ(J)
      DO 250 K=1,NPHAS
250   S=S-CAJ(K,J)*FA(K)
300   CLJ(J)=S
      RETURN
      END

      SUBROUTINE MATEQ(A,N,Y,DET,NDIM)
      INTEGER N,NDIM,I,J,K,IMX,ICHG(40)
      REAL*4 A(NDIM,NDIM),Y(NDIM),DET,AMX,TEMP
      DET=1.0
      DO 10 K=1,N
      AMX=ABS(A(K,K))
      IMX=K
      DO 1 I=K
      IF(ABS(A(I,K)).LE.AMX) GOTO 1
      AMX=ABS(A(I,K))
      IMX=I
1     CONTINUE
      IF(AMX.LT.1.E-8) THEN
        DET=0.0
        RETURN
      ENDIF
      IF(IMX.NE.K) THEN
        DO 4 J=1,N
        TEMP=A(K,J)
        A(K,J)=A(IMX,J)
4       A(IMX,J)=TEMP
        TEMP=Y(K)
        Y(K)=Y(IMX)
        Y(IMX)=TEMP
        ICHG(K)=IMX
      ELSE
        ICHG(K)=K
      ENDIF
      AKK=A(K,K)
      DET=DET*AKK
      A(K,K)=1.0/AKK
      DO 7 J=1,N
7       A(K,J)=A(K,J)*A(K,K)
      Y(K)=Y(K)*A(K,K)
      DO 9 I=1,N
      IF(I.EQ.K) GOTO 9
      AKI=A(I,K)
      DO 8 J=1,N
8       A(I,J)=A(I,J)-AKI*A(K,J)
9       Y(I)=Y(I)-AKI*Y(K)
10    CONTINUE
      DO 13 K=1,N
      L=N+1-K
      KI=ICHG(L)
      IF(L.EQ.KI) GOTO 13
      DO 12 I=1,N
12    TEMP=A(I,L)
      A(I,L)=A(I,KI)
      A(I,KI)=TEMP
13    CONTINUE
      RETURN
      END

      SUBROUTINE PRNTER(MODES,TEMP,FLR,NERR,NUPAGE)
      INTEGER MODES(6),NUPAGE,NERR
      REAL*4 TEMP,FLR
      COMMON/SYSIO/ NCON, NLST, NDSK
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10), DAJ(10,20)
      COMMON RJ(20), QA(10,10)
      INTEGER NWRT
      CALL CIMPL
      NWRT=NCON
      IF(MODES(4).NE.0) GOTO 300
      IF(NUPAGE.NE.0) THEN
        WRITE(NWRT,9200) (PNAMEA(J),J=1,NPHAS)
9200    FORMAT(1H0,'SYS',4X,'LIQ ',3(3X,A4),5X,5(3X,A4))
      ENDIF
      WRITE(NWRT,9210) FL,(FA(J),J=1,NPHAS)
9210  FORMAT(' FRAC ',7X,4F7.4,5X,5F7.4)
      DO 220 K=1,NCOMPT
220   WRITE(NWRT,9220) CNAME(K),CSJ(K),CLJ(K),(CAJ(J,K),J=1,NPHAS)
9220  FORMAT(1H ,A4,1X,5F7.4,5X,5F7.4)
      WRITE(NWRT,9230) TEMP,FLR,SPARM(2)
9230  FORMAT(1H ,'TEMP=',F10.4,' P(kbar)=',F6.2,5X,'FLR =',F10.4)
      WRITE(NWRT,9240) (SNAMES(K),SPARM(K),K=1,NS)
9240  FORMAT(1H ,4(A4,'=',F10.4,5X))
      GOTO 400
300   IF(NUPAGE.NE.0) THEN
        WRITE(NWRT,9300) (PNAMEA(K),K=1,NPHAS),(CNAMEJ(J),J=1,NCOMPT)
9300    FORMAT(1H0,'TEMP P(kbar)',4X,'FLR',4X,3(3X,A4),5X,5(3X,A4))
      ENDIF
      WRITE(NWRT,9310) TEMP,SPARM(2),FL,(FA(J),J=1,NPHAS),(CLJ(J),J=1,NCOMPT)
9310  FORMAT(1H ,F5.0,2X,F6.2,5F7.4,3(5X,5F7.4))
400   IF(NERR.EQ.1) WRITE(NWRT,9400)
9400  FORMAT('0MATRIX INVERSION PROBLEM IN STATE')
      IF(NERR.EQ.2) WRITE(NWRT,9410)
9410  FORMAT('0MAXIMUM ITERATIONS REACHED IN STATE')
      IF(MODES(3).EQ.0) GOTO 500
      NWRT=NLST
      NUPAGE=0
      GOTO 100
500   NUPAGE=0
      RETURN
      END

      SUBROUTINE STATE(NL,LIST,NERR)
      INTEGER NL,LIST(10),NERR
      REAL*4 DFA(10)
      COMMON NS, SNAMES(5), SPARM(5)
      COMMON NPHAS,NCOMP,NCIMP,NCOMPT,NPDIM,NCDIM
      COMMON PNAMEA(10),CNAMEJ(20)
      COMMON FL, FA(10)
      COMMON CSJ(20),CLJ(20),CAJ(10,20)
      COMMON TA(10),UAJ(10,20)
      COMMON FKDAJ(10,20)
      COMMON IMPDIM, IMPL(10), IMCL(10), DA0(10,20)
      COMMON RJ(20), QA(10), PAB(10,10)
      INTEGER ITER,MAX
      REAL*4 TOL,TST
      DATA MAX/20/,TOL/1.E-5/
      NERR=0
      CALL KDCALC(1)
      DO 500 ITER=1,MAX
      DO 100 J=1,NCOMP
      T=1.0
      DO 50 L=1,NPHAS
50    T=T+FA(L)*(FKDAJ(L,J)-1.0)
100   RJ(J)=1.0/T
      CL(J)=CSJ(J)*RJ(J)
      DO 60 L=1,NPHAS
60    CA(L,J)=FKDAJ(L,J)*CL(J)
      NL=0
      DO 200 L=1,NPHAS
      TQ=-TA(L)
      DO 150 J=1,NCOMP
150   TQ=TQ+UA(L,J)*CA(L,J)
      QA(L)=TQ
      CALL STOICH(L,ISOK)
      IF(TQ.GT.0.0 .OR. ISOK.EQ.0) GOTO 200
      NL=NL+1
      LIST(NL)=L
200   CONTINUE
      IF(NL.EQ.0) GOTO 700
      DO 300 J=1,NL
      L=LIST(J)
      DFA(J)=QA(L)
      DO 280 K=1,NL
      M=LIST(K)
      T=0.0
      DO 260 I=1,NCOMP
      TU=UA(L,I)
      IF(TU.EQ.0.0) GOTO 260
      T=T+TU*CA(M,I)*RJ(I)*RJ(I)*(FKDAJ(M,I)-1.0)
260   CONTINUE
      PAB(J,K)=-T
280   CONTINUE
300   CONTINUE
      CALL MATEQ(PAB,NL,DFA,DET,NPDIM)
      IF(ABS(DET).LT.1.E-10) GOTO 900
      TST=0.0
      FL=1.0
      DO 350 J=1,NL
      L=LIST(J)
      T=FA(L)+DFA(J)
      IF(T.LT.0.0) T=0.0
      IF(T.GT.0.9999) T=0.9999
      TST=TST+ABS(FA(L)-T)
      FA(L)=T
      FL=FL-T
350   CONTINUE
      IF(TST.LE.TOL) GOTO 700
      CALL KDCALC(3)
500   CONTINUE
      NERR=2
      RETURN
700   NERR=0
      RETURN
900   NERR=1
      RETURN
      END
